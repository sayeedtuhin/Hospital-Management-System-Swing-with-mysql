public void  displayInTable(){
    if(dtable.getRowCount()>0){
            for (int i = dtable.getRowCount()-1; i > -1; i--) {
                model.removeRow(i);                
            }
        }
        Connection con = null;
        Statement st = null;
        ResultSet rs = null;
        try {
         con = DatabaseHelper.getDBConnection();
         st = con.createStatement();
          rs = st.executeQuery("select * from doctor ");
           while (rs.next()) {
                model.addRow(new Object[]{rs.getString("id"), rs.getString("Doctor_id"), rs.getString("Joining_Date"), rs.getString("Name"), rs.getString("Age"), rs.getString("Gender"),rs.getString("Blood_Group"), rs.getString("Depertment"), rs.getString("Phone"), rs.getString("Email"), rs.getString("Maritial_Status"), rs.getString("Address"), rs.getString("Room")});

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        finally{
            try {
                 con.close();
                st.close();
            } catch (SQLException e) {
                System.out.println(e);
            }
        }
    }
